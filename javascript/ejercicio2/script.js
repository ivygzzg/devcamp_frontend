var x = prompt("Ingresa un numero del 0 al 6")
x = parseInt (x)
switch (x) {
    case 0:
        console.log("domingo");
        break;
    case 1:
    console.log("lunes");
        break;
    case 2:
    console.log("martes");
        break;
    case 3:
    console.log("miercoles");
        break;
    case 4:
    console.log("jueves");
        break;
    case 5:
    console.log("viernes");
        break;
    case 6:
    console.log("sabado");
    break;

    default:
    console.log("Numero no valido")
    

}
 







